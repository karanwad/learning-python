# write a function that will take a word and print out all the vowels in the word 

def vowels(word):
    for x in word:
        if x == 'a':
            print(x)
        if x == 'e':
            print(x)
        if x == 'i':
            print(x)
        if x == 'o':
            print(x)
        if x == 'u':
            print(x)
        if x == 'y':
            print(x)
    
vowels('Charlie')
